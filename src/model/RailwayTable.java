package model;

public class RailwayTable {
	private int RID;
	private String Deptime;
	private String Arrtime;
	private String SNameTha;
	private String SNameEng;
	private String DNameTha;
	private String DNameEng;
	public int getRID() {
		return RID;
	}
	public void setRID(int rID) {
		RID = rID;
	}
	public String getDeptime() {
		return Deptime;
	}
	public void setDeptime(String deptime) {
		Deptime = deptime;
	}
	public String getArrtime() {
		return Arrtime;
	}
	public void setArrtime(String arrtime) {
		Arrtime = arrtime;
	}
	public String getSNameTha() {
		return SNameTha;
	}
	public void setSNameTha(String sNameTha) {
		SNameTha = sNameTha;
	}
	public String getSNameEng() {
		return SNameEng;
	}
	public void setSNameEng(String sNameEng) {
		SNameEng = sNameEng;
	}
	public String getDNameTha() {
		return DNameTha;
	}
	public void setDNameTha(String dNameTha) {
		DNameTha = dNameTha;
	}
	public String getDNameEng() {
		return DNameEng;
	}
	public void setDNameEng(String dNameEng) {
		DNameEng = dNameEng;
	}
}
