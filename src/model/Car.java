package model;

public class Car {
	private int CarNo;
	private int CarType;
	private int Price;
	private int RID;

	public int getCarNo() {
		return CarNo;
	}

	public void setCarNo(int carNo) {
		CarNo = carNo;
	}

	public int getCarType() {
		return CarType;
	}

	public void setCarType(int carType) {
		CarType = carType;
	}

	public int getPrice() {
		return Price;
	}

	public void setPrice(int price) {
		Price = price;
	}

	public int getRID() {
		return RID;
	}

	public void setRID(int rID) {
		RID = rID;
	}

}