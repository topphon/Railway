package model;


public class SourceDestination {

	private int SID;
	private int RID;
	private int SDSelect;
	public int getSID() {
		return SID;
	}
	public void setSID(int sID) {
		SID = sID;
	}
	public int getRID() {
		return RID;
	}
	public void setRID(int rID) {
		RID = rID;
	}
	public int getSDSelect() {
		return SDSelect;
	}
	public void setSDSelect(int sDSelect) {
		SDSelect = sDSelect;
	}
	
}