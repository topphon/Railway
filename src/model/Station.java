package model;



public class Station {
	private int SID;
	private String SNameTha;
	private String SNameEng;
	public int getSID() {
		return SID;
	}
	public void setSID(int sID) {
		SID = sID;
	}
	public String getSNameTha() {
		return SNameTha;
	}
	public void setSNameTha(String sNameTha) {
		SNameTha = sNameTha;
	}
	public String getSNameEng() {
		return SNameEng;
	}
	public void setSNameEng(String sNameEng) {
		SNameEng = sNameEng;
	}
	
}